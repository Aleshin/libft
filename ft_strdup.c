#include "libft.h"

char	*ft_strdup(const char *s1)
{
	unsigned int	i;
	char			*dup;

	dup = (char *)malloc((ft_strlen(s1) + 1) * sizeof(char));
	if (dup == NULL)
		return (0);
	i = 0;
	while (s1[i] != '\0')
	{
		dup[i] = s1[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
